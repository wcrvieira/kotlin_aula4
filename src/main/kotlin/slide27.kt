fun main() {
    val myNum = 5
    val myDoubleNum = 5.99
    val myLetter = 'D'
    val myBoolean = true
    val myText = "Hello"

    // Declaração de variáveis em Kotlin e uso de interpolação
    println("O valor $myNum, poderia ser $myDoubleNum")
    println("A letra $myLetter, poderia fazer parte do texto $myText, se for $myBoolean")
}